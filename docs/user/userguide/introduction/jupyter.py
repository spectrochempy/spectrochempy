# ## Using `Jupyter Lab`
#
# Go to your favorite user document folder (*e.g.,* `$HOME/workspace/) or
# any other folder you want to use to store your work).
#
#     $ cd ~/workspace
#
#
# Open a terminal and issue the following command:
#
#     $ jupyter lab
#
# Your default browser should now be open, and the window should looks similar to this:
#
# <img src="../images/jupyter_lab_home.png" alt="launch" width="690"/>
#
# From there, it is quite easy to create notebooks or to navigate to
