# %% [markdown]
# #### Using `Jupyter Lab`
#
# Open a terminal and issue the following commands:
#
# Go to your favorite user document folder (*e.g.,* `$HOME/workspace/) or
# any other folder you want to use to store your work).
#
# ```bash
# $ cd ~/workspace
# $ jupyter lab
# ```
#
# Your default browser should now be open, and the window should look like this:
#
# <img src="../images/jupyter_lab_home.png" alt="launch" width="690"/>
#
# From there, it is quite easy to create notebooks or to navigate to already existing ones.

