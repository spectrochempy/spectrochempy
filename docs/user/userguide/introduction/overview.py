# Currently **SpectroChemPy** can be used as a library for python scripts.
#
# For ease of use, we recommend using the [JupyterLab](https://jupyterlab.readthedocs.io/en/stable/getting_started/overview.html) application or for those who are more comfortable with programming, writing python scripts in a development environment such as [PyCharm](https://www.jetbrains.com/fr-fr/pycharm/), [VS Code](https://code.visualstudio.com) or [Spyder](https://www.spyder-ide.org). 
